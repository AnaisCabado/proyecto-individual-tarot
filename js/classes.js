class Card {
    constructor(type, name_short, name, value_int, meaning_up, meaning_rev, desc) {
        this.type = type;
        this.name_short = name_short;
        this.name = name;
        this.value_int = value_int;
        this.meaning_up = meaning_up;
        this.meaning_rev = meaning_rev;
        this.desc = desc;
    }
}